package study_final;
/*
final关键字
final表示最终的，不可变得
final可以修饰变量，方法和类
final修饰的类无法继承
final修饰的方法无法覆盖
final修饰的变量只能赋值一次
final的引用一单指向某个对象，则不能再重新指向其他对象，但是该引用指向的对象内部的数据是可以修改的
final修饰的实例变量必须手动初始化，不能使用系统默认值
final修饰的实例变量一般和static联合使用，称为常量
 */
